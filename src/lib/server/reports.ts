// Loads frontmatter-format markdown report files (YAML frontmatter +
// markdown body) and runs `marked` for HTML. Non-frontmatter files are
// rejected with an explicit error — the legacy structured-report format is
// no longer supported. The public loadReport wraps the implementation with
// a defensive ReportData.safeParse + fallback layer.

import 'server-only';
import { existsSync, readdirSync, readFileSync } from 'node:fs';
import { join } from 'node:path';
import yaml from 'js-yaml';
import { Marked } from 'marked';
import { z } from 'zod';
